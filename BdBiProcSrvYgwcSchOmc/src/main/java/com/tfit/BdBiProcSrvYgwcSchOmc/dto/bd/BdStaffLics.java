package com.tfit.BdBiProcSrvYgwcSchOmc.dto.bd;

public class BdStaffLics {
	String rmcName;
	String staffName;
	String ppName;
	int licType;
	String licNo;
	String licStartDate;
	String licExpireDate;
	int licStatus;
	
	public String getRmcName() {
		return rmcName;
	}
	public void setRmcName(String rmcName) {
		this.rmcName = rmcName;
	}
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public String getPpName() {
		return ppName;
	}
	public void setPpName(String ppName) {
		this.ppName = ppName;
	}
	public int getLicType() {
		return licType;
	}
	public void setLicType(int licType) {
		this.licType = licType;
	}
	public String getLicNo() {
		return licNo;
	}
	public void setLicNo(String licNo) {
		this.licNo = licNo;
	}
	public String getLicStartDate() {
		return licStartDate;
	}
	public void setLicStartDate(String licStartDate) {
		this.licStartDate = licStartDate;
	}
	public String getLicExpireDate() {
		return licExpireDate;
	}
	public void setLicExpireDate(String licExpireDate) {
		this.licExpireDate = licExpireDate;
	}
	public int getLicStatus() {
		return licStatus;
	}
	public void setLicStatus(int licStatus) {
		this.licStatus = licStatus;
	}
}
