public class Announces {
	String annId;
	String announcName;
	String orgName;
    String announcDate;
    
	public String getAnnId() {
		return annId;
	}
	public void setAnnId(String annId) {
		this.annId = annId;
	}
	public String getAnnouncName() {
		return announcName;
	}
	public void setAnnouncName(String announcName) {
		this.announcName = announcName;
	}	
	public String getOrgName() {
		return orgName;
	}
	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}
	public String getAnnouncDate() {
		return announcDate;
	}
	public void setAnnouncDate(String announcDate) {
		this.announcDate = announcDate;
	}
}
