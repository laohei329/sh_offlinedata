package com.tfit.BdBiProcSrvYgwcSchOmc.dto.im;

public class WasteOils {
	String recDate;
	String distName;
	int rmcNum;
	String rcNum;
	
	public String getRecDate() {
		return recDate;
	}
	public void setRecDate(String recDate) {
		this.recDate = recDate;
	}
	public String getDistName() {
		return distName;
	}
	public void setDistName(String distName) {
		this.distName = distName;
	}
	public int getRmcNum() {
		return rmcNum;
	}
	public void setRmcNum(int rmcNum) {
		this.rmcNum = rmcNum;
	}
	public String getRcNum() {
		return rcNum;
	}
	public void setRcNum(String rcNum) {
		this.rcNum = rcNum;
	}
}
